#!/usr/bin/env python3
"""Generate a markdown report of recent PyPI download statistics for qmcpy."""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

import pypistats


PACKAGE = "qmcpy"
OUTPUT = Path("stats/pypi_downloads.md")


def ensure_dict(payload: str | dict) -> dict:
    """Return a JSON payload as a Python dict."""
    if isinstance(payload, str):
        return json.loads(payload)
    if isinstance(payload, dict):
        return payload
    raise TypeError(f"Expected str or dict payload, got {type(payload).__name__}")


def extract_recent_total(payload: dict) -> int:
    """Extract last_month downloads from pypistats recent payload."""
    data = payload.get("data", {})
    if isinstance(data, dict):
        return int(data.get("last_month", 0))
    return 0


def extract_overall_rows(payload: dict) -> list[dict]:
    """Extract the daily time-series rows from pypistats overall payload."""
    data = payload.get("data", [])
    if isinstance(data, list):
        return [row for row in data if isinstance(row, dict)]
    return []


def extract_overall_total(rows: list[dict]) -> int:
    """Sum downloads across all daily rows."""
    return int(sum(int(row.get("downloads", 0)) for row in rows))


def extract_date_range(rows: list[dict]) -> tuple[str | None, str | None]:
    """Extract the first and last dates present in the daily series."""
    dates = [row.get("date") for row in rows if isinstance(row.get("date"), str)]
    if not dates:
        return None, None
    return min(dates), max(dates)


def format_int(value: int) -> str:
    """Format an integer with thousands separators."""
    return f"{value:,}"


def build_markdown(
    package: str,
    generated: str,
    recent_total: int,
    overall_total: int,
    start_date: str | None,
    end_date: str | None,
) -> str:
    """Build the markdown report."""
    if start_date and end_date:
        range_line = (
            f"- **Downloads from {start_date} through {end_date}:** "
            f"{format_int(overall_total)}"
        )
        notes_line_1 = "- This total is computed from the daily time series returned by PyPI Stats."
    else:
        range_line = f"- **Recent daily-series downloads:** {format_int(overall_total)}"
        notes_line_1 = (
            "- This total is computed from the available daily time series returned by PyPI Stats."
        )

    return f"""# PyPI Download Statistics

_Auto-generated by GitHub Actions on {generated}._

## Package
- `{package}`

## Summary

- **Last month downloads:** {format_int(recent_total)}
{range_line}

## Notes

{notes_line_1}
- This measures recent activity, not lifetime downloads.
- PyPI Stats data is updated approximately once per day.

## Reproduce locally

```bash
python -c "import pypistats; print(pypistats.recent('{package}', format='json'))"
python -c "import pypistats; print(pypistats.overall('{package}', mirrors='with', format='json'))"
```
"""


def main() -> None:
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)

    recent_payload = ensure_dict(pypistats.recent(PACKAGE, format="json"))
    overall_payload = ensure_dict(pypistats.overall(PACKAGE, mirrors="with", format="json"))

    recent_total = extract_recent_total(recent_payload)
    overall_rows = extract_overall_rows(overall_payload)
    overall_total = extract_overall_total(overall_rows)
    start_date, end_date = extract_date_range(overall_rows)

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    content = build_markdown(
        package=PACKAGE,
        generated=generated,
        recent_total=recent_total,
        overall_total=overall_total,
        start_date=start_date,
        end_date=end_date,
    )

    OUTPUT.write_text(content, encoding="utf-8")


if __name__ == "__main__":
    main()
