#!/usr/bin/env python3
"""Generate a markdown report of recent PyPI download statistics for qmcpy."""

from __future__ import annotations

import json
from datetime import datetime, timedelta, timezone
from pathlib import Path

import pypistats


PACKAGE = "qmcpy"
OUTPUT = Path("stats/pypi_downloads.md")


def ensure_dict(payload: str | dict) -> dict:
    """Return a JSON payload as a Python dict."""
    if isinstance(payload, str):
        return json.loads(payload)
    if isinstance(payload, dict):
        return payload
    raise TypeError(f"Expected str or dict payload, got {type(payload).__name__}")


def extract_overall_rows(payload: dict) -> list[dict]:
    """Extract dated daily rows for the with_mirrors category."""
    data = payload.get("data", [])
    if not isinstance(data, list):
        return []

    rows: list[dict] = []
    for row in data:
        if not isinstance(row, dict):
            continue
        if row.get("category") != "with_mirrors":
            continue
        if not isinstance(row.get("date"), str):
            continue
        rows.append(row)

    return rows


def extract_overall_total(rows: list[dict]) -> int:
    """Sum downloads across all daily rows."""
    return int(sum(int(row.get("downloads", 0)) for row in rows))


def extract_date_range(rows: list[dict]) -> tuple[str | None, str | None]:
    """Extract the first and last dates present in the daily series."""
    dates = [row["date"] for row in rows if isinstance(row.get("date"), str)]
    if not dates:
        return None, None
    return min(dates), max(dates)


def extract_recent_30_day_total(rows: list[dict]) -> tuple[int, str | None, str | None]:
    """Compute downloads over the most recent 30 days in the daily series."""
    dated_rows: list[tuple[datetime, int]] = []
    for row in rows:
        date_str = row.get("date")
        downloads = row.get("downloads", 0)
        if not isinstance(date_str, str):
            continue
        try:
            day = datetime.strptime(date_str, "%Y-%m-%d")
        except ValueError:
            continue
        dated_rows.append((day, int(downloads)))

    if not dated_rows:
        return 0, None, None

    end_day = max(day for day, _ in dated_rows)
    start_day = end_day - timedelta(days=29)
    total = sum(downloads for day, downloads in dated_rows if start_day <= day <= end_day)
    return total, start_day.strftime("%Y-%m-%d"), end_day.strftime("%Y-%m-%d")


def format_int(value: int) -> str:
    """Format an integer with thousands separators."""
    return f"{value:,}"


def build_markdown(
    package: str,
    generated: str,
    recent_30_day_total: int,
    recent_30_day_start: str | None,
    recent_30_day_end: str | None,
    overall_total: int,
    start_date: str | None,
    end_date: str | None,
) -> str:
    """Build the markdown report."""
    if start_date and end_date:
        range_line = (
            f"- **Downloads from {start_date} through {end_date}:** "
            f"{format_int(overall_total)}"
        )
        notes_line_1 = "- This total is computed from the daily PyPI Stats time series including mirrors."
    else:
        range_line = f"- **Recent daily-series downloads:** {format_int(overall_total)}"
        notes_line_1 = (
            "- This total is computed from the available daily PyPI Stats time series including mirrors."
        )

    return f"""# PyPI Download Statistics

_Auto-generated by GitHub Actions on {generated}._

## Package
- `{package}`

## Summary

- **Downloads from {recent_30_day_start} through {recent_30_day_end} (most recent 30 days):** {format_int(recent_30_day_total)}
{range_line}

## Notes

{notes_line_1}
- The 30-day total is computed from the same daily PyPI Stats time series, so the date range is explicit.
- This measures recent activity, not lifetime downloads.
- PyPI Stats daily data covers the most recent 180 days.

## Reproduce locally

```bash
python -c "import pypistats; print(pypistats.recent('{package}', format='json'))"
python -c "import pypistats; print(pypistats.overall('{package}', total='daily', mirrors='with', format='json'))"
```
"""


def main() -> None:
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)

    overall_payload = ensure_dict(
        pypistats.overall(PACKAGE, total="daily", mirrors="with", format="json")
    )

    overall_rows = extract_overall_rows(overall_payload)
    overall_total = extract_overall_total(overall_rows)
    start_date, end_date = extract_date_range(overall_rows)
    recent_30_day_total, recent_30_day_start, recent_30_day_end = extract_recent_30_day_total(overall_rows)

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    content = build_markdown(
        package=PACKAGE,
        generated=generated,
        recent_30_day_total=recent_30_day_total,
        recent_30_day_start=recent_30_day_start,
        recent_30_day_end=recent_30_day_end,
        overall_total=overall_total,
        start_date=start_date,
        end_date=end_date,
    )

    OUTPUT.write_text(content, encoding="utf-8")


if __name__ == "__main__":
    main()
